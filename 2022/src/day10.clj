(def enumerate (partial map-indexed list))

(defn render-cpu-history [cpu-history]
  (doseq [line (partition 40 cpu-history)]
    (doseq [[x cpu] (enumerate line)]
      (if (<= (dec (:x cpu)) x (inc (:x cpu)))
        (print "#")
        (print " ")))
    (println)))

(defn sol2 [instrs]
  (->> instrs
       (step-history init-cpu)
       (render-cpu-history)))

