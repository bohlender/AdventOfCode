(defn parse-instr [line]
  (let [[op & args] (string/split line #"\s")]
    (case op
      "noop" {:op     (keyword op)
              :cycles 1}
      "addx" {:op     (keyword op)
              :cycles 2
              :val    (Integer/parseInt (first args))})))

(defn parse [s]
  (->> (string/split-lines s)
       (map parse-instr)))

; ==============================================================================
; Part 1
; ==============================================================================
(defrecord Cpu [x])
(def init-cpu (->Cpu 1))

(defn step [instr cpu]
  (case (:op instr)
    :noop cpu
    :addx (update cpu :x + (:val instr))))

(defn step-history [cpu instrs]
  (if-some [{:keys [cycles] :as instr} (first instrs)]
    (->> (rest instrs)
         (step-history (step instr cpu))
         (concat (repeat cycles cpu)))
    (list cpu)))

(defn signal-strengths [cpu-history]
  (->> cpu-history
       (map-indexed (fn [cycle cpu] [(inc cycle) cpu]))     ; start count at 1
       (drop 19)
       (take-nth 40)
       (map (fn [[cycle cpu]] (* cycle (:x cpu))))))

(defn sol1 [instrs]
  (->> instrs
       (step-history init-cpu)
       signal-strengths
       (reduce +)))

